// ============================================================

function collectApiEndpoints(pages: PageExportData[]): ApiEndpointConfig[] {
  const endpoints = new Map<string, ApiEndpointConfig>();

  for (const page of pages) {
    collectComponentEndpoints(page.definition.components, endpoints);
  }

  return Array.from(endpoints.values());
}

function collectComponentEndpoints(
  components: ComponentInstance[],
  endpoints: Map<string, ApiEndpointConfig>
): void {
  for (const component of components) {
    if (component.dataSource?.type === 'api' && component.dataSource.endpoint) {
      const endpoint = component.dataSource.endpoint;
      if (!endpoints.has(endpoint)) {
        const parts = endpoint.split('/').filter(Boolean);
        const methodName = 'get' + parts.map(p =>
          p.charAt(0).toUpperCase() + p.slice(1)
        ).join('');
        endpoints.set(endpoint, {
          endpoint,
          dataPath: component.iteratorConfig?.dataPath || 'items',
          controllerName: 'ApiDataController',
          methodName,
          routePath: endpoint,
        });
      }
    }
    if (component.children) {
      collectComponentEndpoints(component.children, endpoints);
    }
  }
}

function collectComponentDataSources(components: ComponentInstance[]): Record<string, any> {
  const dataSources: Record<string, any> = {};

  for (const component of components) {
    if (component.dataSource?.type === 'static' && component.dataSource.staticData) {
      dataSources[`repeater_${component.instanceId}`] = component.dataSource.staticData;
    }
    if (component.children) {
      Object.assign(dataSources, collectComponentDataSources(component.children));
    }
  }

  return dataSources;
}

