function collectStaticImageUrls(components: ComponentInstance[]): Set<string> {
  const urls = new Set<string>();

  for (const component of components) {
    const src = component.props?.src || component.props?.url;
    if (src && typeof src === 'string' && !src.includes('{{')) {
      if (src.startsWith('http://') || src.startsWith('https://') || src.startsWith('/')) {
        urls.add(src);
      }
    }
    const bgImage = component.styles?.backgroundImage;
    if (bgImage && typeof bgImage === 'string') {
      const match = bgImage.match(/url\(['"]?([^'")\s]+)['"]?\)/);
      if (match && match[1] && !match[1].includes('{{')) {
        urls.add(match[1]);
      }
    }
    if (component.children) {
      for (const url of collectStaticImageUrls(component.children)) {
        urls.add(url);
      }
    }
  }

  return urls;
}

async function collectAndAddImages(
  zip: JSZip,
  pages: PageExportData[],
  basePath: string
): Promise<Map<string, string>> {
  const urlMap = new Map<string, string>();
  const allUrls = new Set<string>();

  for (const page of pages) {
    for (const url of collectStaticImageUrls(page.definition.components)) {
      allUrls.add(url);
    }
  }

  for (const url of allUrls) {
    try {
      const response = await fetch(url);
      if (response.ok) {
        const blob = await response.blob();
        const filename = urlToFilename(url);
        const newPath = `/images/${filename}`;
        zip.file(`${basePath}/src/main/resources/static/images/${filename}`, blob);
        urlMap.set(url, newPath);
      }
    } catch {
      // Skip images that can't be fetched
    }
  }

  return urlMap;
}

