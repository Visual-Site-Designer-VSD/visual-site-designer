/**
 * Generate the React SPA app.js bundle source code.
 * This is a standalone React app that:
 * - Fetches page definitions from /api/pages/{name}
 * - Renders components from the page JSON
 * - Handles client-side routing
 * - Loads plugin bundles dynamically
 */
function generateSpaAppJs(pages: PageExportData[]): string {
  const routes = pages.map(p => {
    const path = p.path === '/' ? '/' : p.path;
    return `  { path: '${path}', pageName: '${p.pageName}' }`;
  }).join(',\n');

  return `/**
 * VSD Site Runtime - React SPA Renderer
 * Auto-generated by Visual Site Designer
 */
(function() {
  'use strict';

  const React = window.React;
  const ReactDOM = window.ReactDOM;
  const { useState, useEffect, useCallback, createContext, useContext } = React;

  // ---- Context for plugin integration ----
  const PluginContextRegistryContext = createContext(new Map());

  // Expose VSD API for plugins
  window.__VSD__ = {
    PluginContextRegistryContext,
    registerPluginContextValue: function(contextId, value) {
      // Will be wired up by the App component
      if (window.__VSD__._registerFn) {
        window.__VSD__._registerFn(contextId, value);
      }
    },
    unregisterPluginContextValue: function(contextId) {
      if (window.__VSD__._unregisterFn) {
        window.__VSD__._unregisterFn(contextId);
      }
    }
  };

  // ---- Route definitions ----
  const ROUTES = [
${routes}
  ];

  // ---- Component Renderer ----
  function ComponentRenderer({ component, dataSources }) {
    const { componentId, props, styles, children, instanceId, templateBindings } = component;
    const style = styles || {};
    const key = 'component-' + instanceId;

    // Resolve template bindings
    const resolvedProps = { ...props };
    if (templateBindings && dataSources) {
      for (const [propName, template] of Object.entries(templateBindings)) {
        if (typeof template === 'string' && template.includes('{{')) {
          resolvedProps[propName] = resolveTemplate(template, dataSources);
        }
      }
    }

    // Check for plugin-rendered component
    const pluginRenderer = window.__VSD_RENDERERS__ && window.__VSD_RENDERERS__[componentId];
    if (pluginRenderer) {
      return React.createElement(pluginRenderer, {
        key, ...resolvedProps, style,
        children: renderChildren(children, dataSources)
      });
    }

    // Built-in component rendering
    switch (componentId) {
      case 'Label':
        return renderLabel(key, resolvedProps, style);
      case 'Button':
        return renderButton(key, resolvedProps, style, component.events);
      case 'Image':
        return renderImage(key, resolvedProps, style);
      case 'Container':
      case 'ScrollableContainer':
        return renderContainer(key, resolvedProps, style, children, dataSources);
      case 'Navbar':
      case 'NavbarDefault':
      case 'NavbarCentered':
      case 'NavbarMinimal':
      case 'NavbarDark':
      case 'NavbarGlass':
      case 'NavbarSticky':
        return renderNavbar(key, resolvedProps, style);
      default:
        return renderGeneric(key, componentId, resolvedProps, style, children, dataSources);
    }
  }

  function renderChildren(children, dataSources) {
    if (!children || children.length === 0) return null;
    return children.map(function(child) {
      return React.createElement(ComponentRenderer, {
        key: child.instanceId, component: child, dataSources: dataSources
      });
    });
  }

  function resolveTemplate(template, dataSources) {
    return template.replace(/\\{\\{([^}]+)\\}\\}/g, function(_, path) {
      return getNestedValue(dataSources, path) || '';
    });
  }

  function getNestedValue(obj, path) {
    return path.split('.').reduce(function(o, k) { return o && o[k]; }, obj);
  }

  // ---- Built-in Renderers ----

  function renderLabel(key, props, style) {
    const tagMap = { h1:'h1', h2:'h2', h3:'h3', h4:'h4', h5:'h5', h6:'h6', paragraph:'p', span:'span', label:'label' };
    const tag = tagMap[props.variant] || 'span';
    return React.createElement(tag, { key, style, className: 'component label' }, props.text || '');
  }

  function renderButton(key, props, style, events) {
    const clickEvent = events && events.find(function(e) { return e.eventType === 'onClick'; });
    const onClick = function() {
      if (clickEvent && clickEvent.action && clickEvent.action.type === 'navigate' && clickEvent.action.config && clickEvent.action.config.url) {
        navigateTo(clickEvent.action.config.url);
      }
    };

    if (clickEvent && clickEvent.action && clickEvent.action.type === 'navigate') {
      return React.createElement('a', {
        key, style, className: 'component button',
        href: clickEvent.action.config.url,
        onClick: function(e) { e.preventDefault(); onClick(); }
      }, props.text || 'Click Me');
    }
    return React.createElement('button', { key, style, className: 'component button', onClick: onClick },
      props.text || 'Click Me');
  }

  function renderImage(key, props, style) {
    const src = props.src || props.url || '';
    const alt = props.alt || '';
    const objectFit = props.objectFit || 'cover';
    const objectPosition = props.objectPosition || 'center';

    const containerStyle = { ...style, position: 'relative', overflow: 'hidden' };
    const imgStyle = { width: '100%', height: '100%', objectFit: objectFit, objectPosition: objectPosition };

    return React.createElement('div', { key, style: containerStyle, className: 'component image-container' },
      React.createElement('img', { src: src, alt: alt, style: imgStyle, loading: 'lazy' })
    );
  }

  function renderContainer(key, props, style, children, dataSources) {
    const layoutMode = props.layoutMode || 'flex-column';
    const layoutStyles = getLayoutStyles(layoutMode, props);
    const mergedStyle = { ...layoutStyles, ...style };

    return React.createElement('div', { key, style: mergedStyle, className: 'component container' },
      renderChildren(children, dataSources)
    );
  }

  function renderNavbar(key, props, style) {
    const brandName = props.brandName || props.brand || 'Site';
    const items = props.items || props.navItems || [];
    const navStyle = {
      display: 'flex', alignItems: 'center', justifyContent: 'space-between',
      padding: '12px 24px', ...style
    };

    return React.createElement('nav', { key, style: navStyle, className: 'component navbar' },
      React.createElement('a', {
        href: '/', className: 'navbar-brand',
        style: { fontWeight: 'bold', fontSize: '1.25rem', textDecoration: 'none', color: 'inherit' },
        onClick: function(e) { e.preventDefault(); navigateTo('/'); }
      }, brandName),
      React.createElement('div', { style: { display: 'flex', gap: '16px' } },
        items.map(function(item, i) {
          return React.createElement('a', {
            key: i, href: item.url || item.href || '#',
            style: { textDecoration: 'none', color: 'inherit' },
            onClick: function(e) {
              if (item.url || item.href) { e.preventDefault(); navigateTo(item.url || item.href); }
            }
          }, item.label || item.text || '');
        })
      )
    );
  }

  function renderGeneric(key, componentId, props, style, children, dataSources) {
    return React.createElement('div', {
      key, style: style, className: 'component ' + componentId.toLowerCase()
    }, renderChildren(children, dataSources));
  }

  function getLayoutStyles(layoutMode, props) {
    var base = { display: 'flex', boxSizing: 'border-box' };
    switch (layoutMode) {
      case 'flex-row': return { ...base, flexDirection: 'row', gap: props.gap || '8px' };
      case 'flex-column': return { ...base, flexDirection: 'column', gap: props.gap || '8px' };
      case 'flex-wrap': return { ...base, flexDirection: 'row', flexWrap: 'wrap', gap: props.gap || '8px' };
      case 'grid-2col': return { display: 'grid', gridTemplateColumns: '1fr 1fr', gap: props.gap || '8px' };
      case 'grid-3col': return { display: 'grid', gridTemplateColumns: '1fr 1fr 1fr', gap: props.gap || '8px' };
      case 'grid-4col': return { display: 'grid', gridTemplateColumns: 'repeat(4, 1fr)', gap: props.gap || '8px' };
      default: return { ...base, flexDirection: 'column', gap: props.gap || '8px' };
    }
  }

  // ---- Client-side Router ----
  function navigateTo(path) {
    window.history.pushState({}, '', path);
    window.dispatchEvent(new PopStateEvent('popstate'));
  }

  function useRouter() {
    const [currentPath, setCurrentPath] = useState(window.location.pathname);

    useEffect(function() {
      function handlePopState() { setCurrentPath(window.location.pathname); }
      window.addEventListener('popstate', handlePopState);
      return function() { window.removeEventListener('popstate', handlePopState); };
    }, []);

    return currentPath;
  }

  // ---- Page Component ----
  function Page({ pageName }) {
    const [pageData, setPageData] = useState(null);
    const [loading, setLoading] = useState(true);
    const [error, setError] = useState(null);

    useEffect(function() {
      setLoading(true);
      setError(null);
      fetch('/api/pages/' + pageName, { credentials: 'same-origin' })
        .then(function(res) {
          if (!res.ok) throw new Error('Page not found');
          return res.json();
        })
        .then(function(data) {
          setPageData(data);
          setLoading(false);
        })
        .catch(function(err) {
          setError(err.message);
          setLoading(false);
        });
    }, [pageName]);

    if (loading) {
      return React.createElement('div', { className: 'page-loading', style: {
        display: 'flex', justifyContent: 'center', alignItems: 'center',
        minHeight: '200px', color: '#666'
      }}, 'Loading...');
    }

    if (error) {
      return React.createElement('div', { className: 'page-error', style: {
        display: 'flex', justifyContent: 'center', alignItems: 'center',
        minHeight: '200px', color: '#e53e3e'
      }}, 'Error: ' + error);
    }

    if (!pageData) return null;

    const components = pageData.components || [];
    const dataSources = pageData.data || {};

    return React.createElement('div', { className: 'page', 'data-page': pageName },
      components.map(function(component) {
        return React.createElement(ComponentRenderer, {
          key: component.instanceId, component: component, dataSources: dataSources
        });
      })
    );
  }

  // ---- App Component ----
  function App() {
    const currentPath = useRouter();
    const [contextRegistry, setContextRegistry] = useState(new Map());

    // Wire up plugin context registration
    useEffect(function() {
      window.__VSD__._registerFn = function(contextId, value) {
        setContextRegistry(function(prev) {
          const next = new Map(prev);
          next.set(contextId, value);
          return next;
        });
      };
      window.__VSD__._unregisterFn = function(contextId) {
        setContextRegistry(function(prev) {
          const next = new Map(prev);
          next.delete(contextId);
          return next;
        });
      };
    }, []);

    // Find matching route
    var matchedRoute = null;
    for (var i = 0; i < ROUTES.length; i++) {
      if (ROUTES[i].path === currentPath || ROUTES[i].path === currentPath.replace(/\\/$/, '')) {
        matchedRoute = ROUTES[i];
        break;
      }
    }
    // Default to first route (home) if no match
    if (!matchedRoute && ROUTES.length > 0) {
      // Check for home page
      matchedRoute = ROUTES.find(function(r) { return r.path === '/'; }) || ROUTES[0];
    }

    return React.createElement(
      PluginContextRegistryContext.Provider,
      { value: contextRegistry },
      matchedRoute
        ? React.createElement(Page, { key: matchedRoute.pageName, pageName: matchedRoute.pageName })
        : React.createElement('div', { className: 'page-not-found' }, '404 - Page Not Found')
    );
  }

  // ---- Bootstrap ----
  var root = document.getElementById('root');
  if (root) {
    if (ReactDOM.createRoot) {
      ReactDOM.createRoot(root).render(React.createElement(App));
    } else {
      ReactDOM.render(React.createElement(App), root);
    }
  }
})();
`;
}

