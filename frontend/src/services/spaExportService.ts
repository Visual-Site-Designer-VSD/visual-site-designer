export async function exportAsSpaProject(
  pages: PageExportData[],
  options: Partial<SpaExportOptions> = {}
): Promise<Blob> {
  const defaults: SpaExportOptions = {
    projectName: 'my-site',
    groupId: 'com.example',
    artifactId: 'my-site',
    version: '1.0.0',
    springBootVersion: '3.2.0',
    javaVersion: '21',
    authType: 'none',
    siteRuntimeVersion: '1.0.0-SNAPSHOT',
  };
  const opts: SpaExportOptions = { ...defaults, ...options };
  const basePath = opts.artifactId;
  const pkg = getJavaPackage(opts);
  const pkgPath = pkg.replace(/\./g, '/');

  const zip = new JSZip();

  // 1. Collect and add images
  imageUrlMap = await collectAndAddImages(zip, pages, basePath);

  // 2. Collect API endpoints
  const apiEndpoints = collectApiEndpoints(pages);

  // 3. Generate pom.xml
  zip.file(`${basePath}/pom.xml`, generatePomXml(opts, apiEndpoints.length > 0));

  // 4. Application.java
  zip.file(
    `${basePath}/src/main/java/${pkgPath}/Application.java`,
    generateApplicationJava(opts)
  );

  // 5. application.properties
  zip.file(
    `${basePath}/src/main/resources/application.properties`,
    generateApplicationProperties(opts)
  );

  // 6. Page definition JSON files
  for (const page of pages) {
    const pageDataSources = collectComponentDataSources(page.definition.components);
    const pageJson = {
      pageName: page.pageName,
      title: page.definition.pageName || page.pageName,
      description: '',
      path: page.path,
      components: page.definition.components,
      dataSources: pageDataSources,
    };
    zip.file(
      `${basePath}/src/main/resources/pages/${page.pageName}.json`,
      JSON.stringify(pageJson, null, 2)
    );
  }

  // 7. SPA frontend assets
  const pluginBundles: string[] = [];
  // TODO: In future, detect installed plugins and include their bundles
  // For now, generate with empty plugin list (users can add manually)

  zip.file(
    `${basePath}/src/main/resources/static/index.html`,
    generateIndexHtml(opts.projectName, pluginBundles)
  );
  zip.file(
    `${basePath}/src/main/resources/static/js/app.js`,
    generateSpaAppJs(pages)
  );
  zip.file(
    `${basePath}/src/main/resources/static/css/styles.css`,
    generateBaseCSS()
  );

  // 8. Placeholder for plugins directory
  zip.file(
    `${basePath}/src/main/resources/static/js/plugins/.gitkeep`,
    ''
  );

  // 9. Dockerfile & README
  zip.file(`${basePath}/Dockerfile`, generateDockerfile(opts));
  zip.file(`${basePath}/README.md`, generateReadme(opts, pages));

  return zip.generateAsync({ type: 'blob' });
}

export async function downloadSpaProject(
  pages: PageExportData[],
  options?: Partial<SpaExportOptions>
): Promise<void> {
  const blob = await exportAsSpaProject(pages, options);
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `${options?.artifactId || 'my-site'}-spa.zip`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
